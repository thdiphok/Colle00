 #include "ft_putchar.c"
	
 void rush00(int q, int v)
 {
 	char c;
 	int i =0;
 	while ( i < v)
  {
	  int b =0;
      while ( b < q)
	  {
        if ((i == 0 || i == v-1) && (b == 0 || b== q-1))
		{
          c='o';
		}
        else if (i == 0 || i == v-1)
		{
          c = '-';
		}
        else if (b ==0 || b == q-1)
		{
          c = '|';
		}
        else
		{
		   	c = ' ';
		}
          ft_putchar(c);
		  b++;
	  }
	  i++;
      ft_putchar('\n');
  }
}
